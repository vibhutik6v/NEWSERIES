package AutoboxingUnboxing;

public class Unboxing {
    public static void main(String[] args) {
        Integer a=100;
        Double b=90.3344;
        Character c='A';


        //Unboxing

        int x=a;
        double y=b;
        char z=c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
    
}
