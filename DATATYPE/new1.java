public class new1 {
    public static void main(String[] args) {
        // primitive datatype
        char c='=';
        System.out.println(c);

        boolean bool = true;
        System.out.println(bool);

        byte b=2;
        System.out.println(b);

        short s=5000;
        System.out.println(s);

        int i=1000;
        System.out.println(i);

        long l=20  ;
        System.out.println(l);

        float f= 5.1f;
        System.out.println(f);

        double d=2.3;
        System.out.println(d);


    }
}
